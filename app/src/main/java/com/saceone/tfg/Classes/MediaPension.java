public class MediaPension {

    private int menus;
    private int desayunos;

    public MediaPension(int menus, int desayunos) {
        this.menus = menus;
        this.desayunos = desayunos;
    }

    public int getMenus() {
        return menus;
    }

    public int getDesayunos() {
        return desayunos;
    }
}
